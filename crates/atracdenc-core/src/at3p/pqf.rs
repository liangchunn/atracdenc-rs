//! ATRAC3+ 16-band analysis polyphase filterbank (PQF).
//!
//! Port of `atracdenc/src/atrac/atrac3plus_pqf/atrac3plus_pqf.c` (LGPL-2.1).
//! The 384-tap prototype FIR coefficients are FFmpeg-derived
//! (`atrac3plus_pqf_data.h`, also LGPL-2.1).
//!
//! A frame is 2048 samples → 16 subbands × 128 samples. Output layout is
//! `out[sb * 128 + s]`. The forward filter uses the existing DCT-IV (`Dct4`),
//! matching the C `atde_create_dct4_16(128 * 512.0)` scale.

use crate::dsp::dct::Dct4;

const SUBBANDS_NUM: usize = 16;
const SUBBAND_SIZE: usize = 128;
const PROTO_SZ: usize = 384;
const FRAME_SZ: usize = SUBBANDS_NUM * SUBBAND_SIZE; // 2048
const OVERLAP_SZ: usize = PROTO_SZ - SUBBANDS_NUM; // 368
const FIR_LEN: usize = 12;

/// Build the 384-tap analysis FIR from the FFmpeg IPQF coefficients
/// (mirrors C `init()`).
fn build_fir() -> [f32; PROTO_SZ] {
    let mut fir = [0.0f32; PROTO_SZ];
    for i in 0..16 {
        for j in 0..FIR_LEN {
            if i >= 8 {
                fir[j + 96 + (i - 8) * 12] = FF_IPQF_COEFFS1[j][i];
                fir[j + 288 + (i - 8) * 12] = FF_IPQF_COEFFS2[j][i];
            } else {
                fir[j + 192 + i * 12] = FF_IPQF_COEFFS2[j][i];
                fir[j + i * 12] = FF_IPQF_COEFFS1[j][i];
            }
        }
    }
    fir
}

/// Analysis PQF context (per channel). Mirrors C `struct at3plus_pqf_a_ctx`.
pub struct At3pPqf {
    buf: [f32; FRAME_SZ + OVERLAP_SZ],
    fir: [f32; PROTO_SZ],
    dct: Dct4,
}

impl At3pPqf {
    pub fn new() -> Self {
        Self {
            buf: [0.0; FRAME_SZ + OVERLAP_SZ],
            fir: build_fir(),
            dct: Dct4::new(16, 128.0 * 512.0),
        }
    }

    fn matrixing(&mut self, y: &[f64; 32], samples: &mut [f32], base: usize) {
        let mut yy = [0.0f32; SUBBANDS_NUM];
        for i in 0..8 {
            yy[i] = (y[i + 8] + y[7 - i]) as f32;
            yy[i + 8] = (y[i + 16] + y[31 - i]) as f32;
        }
        let res = self.dct.transform(&yy);
        let mut out = [0.0f32; SUBBANDS_NUM];
        out.copy_from_slice(res);
        for i in 0..SUBBANDS_NUM {
            samples[base + i * SUBBAND_SIZE] = out[SUBBANDS_NUM - 1 - i];
        }
    }

    /// Analyze a 2048-sample frame `input` into `out[16 * 128]` (layout
    /// `out[sb * 128 + s]`).
    pub fn analyse(&mut self, input: &[f32], out: &mut [f32]) {
        assert_eq!(input.len(), FRAME_SZ);
        assert!(out.len() >= FRAME_SZ);

        self.buf[OVERLAP_SZ..OVERLAP_SZ + FRAME_SZ].copy_from_slice(input);

        let mut y = [0.0f64; 32];
        for i in 0..SUBBAND_SIZE {
            // x = &buf[i * 16 ..]
            let x_off = i * SUBBANDS_NUM;
            // SAFETY-free copy: borrow buf slice for vectoring.
            let x: &[f32] = &self.buf[x_off..];
            // vectoring needs an immutable borrow of fir + x; do it inline.
            for k in 0..32 {
                let mut acc = 0.0f64;
                for j in 0..FIR_LEN {
                    acc += self.fir[k * 12 + j] as f64 * x[j * 32 + k] as f64;
                }
                y[k] = acc;
            }
            self.matrixing(&y, out, i);
        }

        // Save overlap: copy last OVERLAP_SZ of frame to the front.
        self.buf.copy_within(FRAME_SZ..FRAME_SZ + OVERLAP_SZ, 0);
    }
}

impl Default for At3pPqf {
    fn default() -> Self {
        Self::new()
    }
}

